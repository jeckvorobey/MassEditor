    public static function normalizeSetting($value)
    {
        $value = (string) $value;
        if (!in_array($value, array(self::AUTO, self::RU, self::EN), true)) {
            return self::AUTO;
        }

        return $value;
    }

    public static function resolveLanguage($setting)
    {
        $setting = self::normalizeSetting($setting);
        if ($setting !== self::AUTO) {
            return $setting;
        }

        $locale = '';
        try {
            $system = wa();
            if ($system && method_exists($system, 'getLocale')) {
                $locale = (string) $system->getLocale();
            }
        } catch (Exception $e) {
            $locale = '';
        }

        return stripos($locale, 'ru') === 0 ? self::RU : self::EN;
    }

    public static function getTexts($language)
    {
        $language = $language === self::EN ? self::EN : self::RU;
        if ($language === self::EN && !isset(self::$texts[self::EN])) {
            self::buildEnglishTexts();
        }

        return self::$texts[$language];
    }

    public static function t($key, $language, $default = null)
    {
        $texts = self::getTexts($language);
        if (isset($texts[$key])) {
            return $texts[$key];
        }

        return $default === null ? $key : $default;
    }

